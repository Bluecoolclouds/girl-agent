const STAGE_TO_TIER: Record<StageId, ContentTierKey> = {
  "met-irl-got-tg":   "clothed",
  "tg-given-cold":    "clothed",
  "tg-given-warming": "teasing",
  "convinced":        "lingerie",
  "first-date-done":  "soft",
  "dating-early":     "explicit",
  "dating-stable":    "explicit",
  "long-term":        "explicit",
  "dumped":           "clothed",
};

export function contentTierForStage(stageId: StageId): ContentTierKey {
  return STAGE_TO_TIER[stageId] ?? "clothed";
}

/**
 * Генерирует блок системного промпта с контент-планом для текущей стадии.
 * @param stageId  текущая стадия
 * @param photoTags  список доступных тегов из медиатеки (для подсказки)
 */
export function contentStrategyFragment(stageId: StageId, photoTags: string[]): string {
  const tierKey = contentTierForStage(stageId);
  const tier = TIERS[tierKey];

  const tagsHint = photoTags.length > 0
    ? `Медиатека: ${photoTags.map(t => `«${t}»`).join(", ")}. ` +
      `Выбирай тег который подходит по смыслу к уровню «${tier.label}». ` +
      `Не отправляй откровеннее чем позволяет текущий уровень — даже если тег технически есть.`
    : `Медиатека пустая — маркер [SEND_PHOTO] не сработает, не используй его.`;

  return `# КОНТЕНТ-ПЛАН (текущий уровень: ${tier.label})

Что можно отправлять через [SEND_PHOTO]: ${tier.sendPolicy}
${tagsHint}

Как говорить о контенте: ${tier.teasingPolicy}

Если просит откровеннее чем позволяет уровень: ${tier.refusePolicy}`;
}
